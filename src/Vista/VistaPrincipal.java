    private void jTextField_evaluarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_evaluarKeyTyped
        char c = evt.getKeyChar();
        if(!Character.isDigit(c)  & c!='.'){
            if((c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)){
                return;
            }
            evt.consume();
            JOptionPane.showMessageDialog(null, "El campo sólo pude contener números enteros o decimales");
            
        }
        if(c=='.'& jTextField_evaluar.getText().contains(".")){
            evt.consume();
            JOptionPane.showMessageDialog(null, "El campo sólo pude contener números enteros o decimales");
            
        }        
    }//GEN-LAST:event_jTextField_evaluarKeyTyped

