    private void jButton_factorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_factorActionPerformed
       if(jComboBox_seleccionarPolinomio.getItemCount()==0){
            JOptionPane.showMessageDialog(null, "Debes agregar un polinomio primero");
            return;
        }
       Polinomio polinomio = new Polinomio(jComboBox_seleccionarPolinomio.getSelectedItem().toString());
       Polinomio factor = new Polinomio(jTextField_factor.getText());
       String mensaje = "";
       if(polinomio.factorPolinomio(factor)==true){
           mensaje = "El "+polinomio.imprimirPolinomio()+ " es factorizable con "+factor.imprimirPolinomio();
       }
       else{
           mensaje = "El "+polinomio.imprimirPolinomio()+ " no es factorizable con "+factor.imprimirPolinomio();
       }        
       JOptionPane.showMessageDialog(null, mensaje);
    }//GEN-LAST:event_jButton_factorActionPerformed

